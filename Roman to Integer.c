#include <stdlib.h>

int value(char c) {
    switch(c) {
        case 'I': return 1;
        case 'V': return 5;
        case 'X': return 10;
        case 'L': return 50;
        case 'C': return 100;
        case 'D': return 500;
        case 'M': return 1000;
        default: return 0;
    }
}

int romanToInt(char * s) {
    int total = 0;
    int prev = 0;

    for (int i = 0; s[i]; i++) {
        int curr = value(s[i]);
        if (curr > prev) {
            total += curr - 2 * prev; // adjust for subtraction rule
        } else {
            total += curr;
        }
        prev = curr;
    }

    return total;
}

